    {
        $users = User::select([
            'id',
            'username',
            'email',
            'role'
        ]);

        return DataTables::of($users)
            ->addColumn('manager', function($user) {
                if (isset($user->manager)) {
                    return $user->manager->username;
                } else {
                    return 'N/A';
                }
            })
            ->make(true);
    }

